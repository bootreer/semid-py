from dataclasses import dataclass

import numpy as np
from numpy._typing import NDArray

from semid_py.graph import MixedGraph


@dataclass
class Graph:
    L: list[int]
    O: list[int]  # noqa: E741
    dim: tuple[int, int]

    htc_id: int
    """
    1: identifiable
    0: non-identifiable
    -1: inconclusive
    """

    global_id: int

    def _symmetricize(self, arr: NDArray):
        return ((arr + arr.T) > 0).astype(int)

    def to_mixed_graph(self) -> MixedGraph:
        L = np.reshape(self.L, self.dim)
        O_sym = self._symmetricize(np.reshape(self.O, self.dim))
        return MixedGraph(d_adj=L, b_adj=O_sym)


